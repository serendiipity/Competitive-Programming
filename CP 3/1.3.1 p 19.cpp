#include <vector>
#include <iostream>
#include <string>
#include <cstring>
using namespace std;

int main(void) {
    int sum = 0;
    vector <int> results;
    string inp;
    while (getline(cin, inp)) {
        char line[(int)inp.size()];
        strcpy(line, inp.c_str());
        char a[2];
        int size = 0;
        int i = 0;
        int j = 0;
        while (size < (int)inp.size() - i) {
            strcpy(a, strtok(&line[j], " "));
            int b = a[0] - 48;
            sum += b;
            size++;
            i++;
            j+=2;
        }
        results.push_back(sum);
        sum = 0;
        size = 0;
    }
    for (auto r : results)
        cout << r << endl;
    return 0;
}
